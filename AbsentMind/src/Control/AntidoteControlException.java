package Control;

/**
 *
 * @author melissajensen
 */
public class AntidoteControlException extends Exception{

    public AntidoteControlException() {
    }

    public AntidoteControlException(String message) {
        super(message);
    }

    public AntidoteControlException(String message, Throwable cause) {
        super(message, cause);
    }

    public AntidoteControlException(Throwable cause) {
        super(cause);
    }

    public AntidoteControlException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);
    }
    
    
}
